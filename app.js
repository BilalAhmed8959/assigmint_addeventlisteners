const box = document.querySelector("#box");
const img = document.querySelector("#img");


const abc = box.addEventListener("mouseover", (Event)=>{
    img.src ="./realistic-light-bulb-with-electricity_23-2149129409 (1).avif"
    Event.innerHTML = abc
})
const cde = box.addEventListener("mouseout", (Event)=>{
    img.src ="./3d-realistic-off-light-bulb-icon-closeup-vector-27407044.jpg"
    Event.innerHTML = cde
})

